package es.umh.poo.RuletaDeLaFortuna.Objetos;



public class Partida {
	
	
	
	private int numeroPartidas; 
	private String jugador;
	private int puntuacionpartida;
	private int puntuaciongeneral;
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	/* Getters y Setters*/
	
	public int getNumeroPartidas() {
		return numeroPartidas;		
	}
	public void setNumeroPartidas(int numeroPartidas) {
		this.numeroPartidas = numeroPartidas;
	}
	public String getJugador() {
		return jugador;
	}
	public void setJugador(String jugador) {
		this.jugador = jugador;
	}
	public int getPuntuacionpartida() {
		return puntuacionpartida;
	}
	public void setPuntuacionpartida(int puntuacionpartida) {
		this.puntuacionpartida = puntuacionpartida;
	}
	public int getPuntuaciongeneral() {
		return puntuaciongeneral;
	}
	public void setPuntuaciongeneral(int puntuaciongeneral) {
		this.puntuaciongeneral = puntuaciongeneral;
	}
	
	

	
	
	
	
	
	
	
	

}